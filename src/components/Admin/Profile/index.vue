    data() {
        return {
            list_login: [],
            auth: '',
            edit_nhan_vien: {}
        }
    },
    mounted() {
        this.layDataDanhLogin()
    },
    methods: {
        layDataDanhLogin() {
            axios
                .get("http://127.0.0.1:8000/api/nhan-vien/data-dang-nhap", {
                    headers: {
                        Authorization: 'Bearer ' + localStorage.getItem("token_nhan_vien")
                    }
                })
                .then((res) => {
                    this.list_login = res.data.data;
                    this.auth = res.data.status;
                })
        },

        capNhatNhanVien() {
            axios
                .post("http://127.0.0.1:8000/api/nhan-vien/update", this.edit_nhan_vien, {
                    headers: {
                        Authorization: 'Bearer ' + localStorage.getItem("token_nhan_vien")
                    }
                })
                .then((res) => {
                    if (res.data.status) {
                        var thong_bao = '<b>Thông báo</b><span style="margin-top: 5px">' + res.data.message + '<span>';
                        this.$toast.success(thong_bao);
                        this.layDataDanhLogin();
                    } else {
                        var thong_bao = '<b>Thông báo</b><span style="margin-top: 5px">' + res.data.message + '<span>';
                        this.$toast.error(thong_bao);
                    }
                })
        },
        formatPhone_0xxx(phone) {
            const digits = phone.replace(/\D/g, '');

            if (digits.length === 10 && digits.startsWith('0')) {
                return digits.replace(/(\d{4})(\d{3})(\d{3})/, '$1.$2.$3');
            }

            return phone; // Trả lại nguyên bản nếu không đúng định dạng mong muốn
        }

    },
}
